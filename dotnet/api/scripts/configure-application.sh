#!/bin/bash

# BASE -> APPLICATION
NEW_APPLICATION_INTERFACES_IEXAMPLESERVICE="$SOLUTION_NAME.Application/Interfaces/IExampleService.cs"
NEW_APPLICATION_MAPPINGS_EXAMPLEMAPPINGPROFILE="$SOLUTION_NAME.Application/Mappings/ExampleMappingProfile.cs"
NEW_APPLICATION_SERVICES_EXAMPLESERVICE="$SOLUTION_NAME.Application/Services/ExampleService.cs"
NEW_APPLICATION_VIEWMODELS_EXAMPLE_EXAMPLEVIEWMODEL="$SOLUTION_NAME.Application/ViewModels/Example/ExampleViewModel.cs"
NEW_APPLICATION_VIEWMODELS_PAGEDRESULT="$SOLUTION_NAME.Application/ViewModels/PagedResult.cs"

sed "s/{{SOLUTION_NAME}}/$SOLUTION_NAME/g" "$PATH_TEMPLATES_BASE_APPLICATION_INTERFACES_IEXAMPLESERVICE" > "$NEW_APPLICATION_INTERFACES_IEXAMPLESERVICE"
sed "s/{{SOLUTION_NAME}}/$SOLUTION_NAME/g" "$PATH_TEMPLATES_BASE_APPLICATION_MAPPINGS_EXAMPLEMAPPINGPROFILE" > "$NEW_APPLICATION_MAPPINGS_EXAMPLEMAPPINGPROFILE"
sed "s/{{SOLUTION_NAME}}/$SOLUTION_NAME/g" "$PATH_TEMPLATES_BASE_APPLICATION_SERVICES_EXAMPLESERVICE" > "$NEW_APPLICATION_SERVICES_EXAMPLESERVICE"
sed "s/{{SOLUTION_NAME}}/$SOLUTION_NAME/g" "$PATH_TEMPLATES_BASE_APPLICATION_VIEWMODELS_EXAMPLE_EXAMPLEVIEWMODEL" > "$NEW_APPLICATION_VIEWMODELS_EXAMPLE_EXAMPLEVIEWMODEL"
sed "s/{{SOLUTION_NAME}}/$SOLUTION_NAME/g" "$PATH_TEMPLATES_BASE_APPLICATION_VIEWMODELS_PAGEDRESULT" > "$NEW_APPLICATION_VIEWMODELS_PAGEDRESULT"

validate_file "$NEW_APPLICATION_INTERFACES_IEXAMPLESERVICE"
validate_file "$NEW_APPLICATION_MAPPINGS_EXAMPLEMAPPINGPROFILE"
validate_file "$NEW_APPLICATION_SERVICES_EXAMPLESERVICE"
validate_file "$NEW_APPLICATION_VIEWMODELS_EXAMPLE_EXAMPLEVIEWMODEL"
validate_file "$NEW_APPLICATION_VIEWMODELS_PAGEDRESULT"

print_info "Created application structures."
